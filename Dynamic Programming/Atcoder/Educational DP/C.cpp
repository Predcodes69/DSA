vi primes(N, 1);
void sieve()
{
    primes[0] = primes[1] = 0;
    for (int i = 2; i * i <= N; i++)
    {
        if (primes[i])
            for (int j = i * i; j <= N; j += i)
                primes[j] = 0;
    }
    for (int i = 2; i < N; i++)
    {
        if (primes[i] == 1)
        {
            primes[i] = i;
            for (int j = i * i; j <= N; j += i)
            {
                if (!primes[j])
                    primes[j] = i; // storing min primes
            }
        }
    }
}
